myKeys :: [(String, X ())]
myKeys = 
    [
        ("M-C-r", spawn "xmonad --recompile")  -- Recompiles xmonad
        , ("M-S-r", spawn "xmonad --restart")    -- Restarts xmonad
        , ("M-C-q", io exitSuccess)              -- Quits xmonad

        -- Run Prompt
        , ("M-p", spawn "dmenu_run -i -p \"Run: \"") -- Dmenu
        ,("M-b", sendMessage ToggleStruts)

        -- Useful programs to have a keybinding for launch
        , ("M-<Return>", spawn myTerminal)
        , ("M-w", spawn myBrowser)

        -- Kill windows
        , ("M-q", kill1)     -- Kill the currently focused client
        , ("M-S-q", killAll)   -- Kill all windows on current workspace

        -- Workspaces
        , ("M-S-<KP_Add>", nextWS)       -- Shifts focused window to next ws
        , ("M-S-<KP_Subtract>", nextWS )  -- Shifts focused window to prev ws

        -- Increase/decrease spacing (gaps)
        , ("C-M1-j", decWindowSpacing 4)         -- Decrease window spacing
        , ("C-M1-k", incWindowSpacing 4)         -- Increase window spacing
        , ("C-M1-h", decScreenSpacing 4)         -- Decrease screen spacing
        , ("C-M1-l", incScreenSpacing 4)         -- Increase screen spacing

        -- Windows navigation
        , ("M-m", windows W.focusMaster)  -- Move focus to the master window
        , ("M-j", windows W.focusDown)    -- Move focus to the next window
        , ("M-k", windows W.focusUp)      -- Move focus to the prev window
        , ("M-S-m", windows W.swapMaster) -- Swap the focused window and the master window
        , ("M-S-j", windows W.swapDown)   -- Swap focused window with next window
        , ("M-S-k", windows W.swapUp)     -- Swap focused window with prev window
        , ("M-<Backspace>", promote)      -- Moves focused window to master, others maintain order
        , ("M-S-<Tab>", rotSlavesDown)    -- Rotate all windows except master and keep focus in place
        , ("M-C-<Tab>", rotAllDown)       -- Rotate all the windows in the current stack

        -- Layouts
        , ("M-<Tab>", sendMessage NextLayout)           -- Switch to next layout
        , ("M-<Space>", sendMessage (MT.Toggle NBFULL) >> sendMessage ToggleStruts) -- Toggles noborder/full

        -- Window resizing
        , ("M-h", sendMessage Shrink)                   -- Shrink horiz window width
        , ("M-l", sendMessage Expand)                   -- Expand horiz window width
        , ("M-M1-j", sendMessage MirrorShrink)          -- Shrink vert window width
        , ("M-M1-k", sendMessage MirrorExpand)          -- Expand vert window width

        
    ]

main :: IO ()
main = do
    -- Launching xmobar 
    h <- spawnPipe "xmobar /home/tryk0/.config/xmobar/xmobarrc0bak"
    xmonad $ def
        {
          modMask            = myModMask
        , terminal           = myTerminal
        , startupHook        = myStartupHook
        , layoutHook         = myLayoutHook
        ,logHook             = dynamicLogWithPP $ def { ppOutput = hPutStrLn h } 
        , handleEventHook    = docksEventHook
        , manageHook         = manageDocks 
        , borderWidth        = myBorderWidth
        , normalBorderColor  = myNormColor
        , focusedBorderColor = myFocusColor
        } `additionalKeysP` myKeys
